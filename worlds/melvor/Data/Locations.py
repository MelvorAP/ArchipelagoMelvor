from enum import IntEnum
from typing import NamedTuple, List, Dict


class LocationType(IntEnum):
    NONE = 0,
    PET = 1,
    ACTION = 2,
    STORE = 3,
    COMBAT = 4,
    SLAYER = 5,
    DUNGEON = 6,
    STRONGHOLD = 7,


class CombatType(IntEnum):
    NONE = 0,
    MELEE = 1,
    RANGED = 2,
    MAGIC = 3,
    ALL = 4


class GameNameSpace(IntEnum):
    NONE = 0,
    DEMO = 1,
    FULL = 2,
    TOTH = 3,
    AOD = 4,
    ITA = 5


class LocationData(NamedTuple):
    name: str
    region_name: str
    reqs: List[str] = []
    namespace: GameNameSpace = GameNameSpace.NONE
    location_type: LocationType = LocationType.NONE
    easy_combat: bool = False
    combat_type: CombatType = CombatType.NONE
    inverted_combat_tree: bool = False

